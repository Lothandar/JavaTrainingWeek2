package Garage;

public class Motorcycle extends Vehicle{
	private int noOfHelmets;
	private int noOfTrumbones;

	public int getNoOfTrumbones() {
		return noOfTrumbones;
	}

	public void setNoOfTrumbones(int noOfTrumbones) {
		this.noOfTrumbones = noOfTrumbones;
	}

	public int getNoOfHelmets() {
		return noOfHelmets;
	}

	public void setNoOfHelmets(int noOfHelmets) {
		this.noOfHelmets = noOfHelmets;
	}
	
	
}
